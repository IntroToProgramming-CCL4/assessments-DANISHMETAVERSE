name = "  \t\n Danish Baloch \t\n" #person name with whitespace at beginning and end
print("Original Name:") #print once so whitespace is displayed
print(name)
print("\nAfter using lstrip():") #print using 3 stripping functions. LSTRIP
print(name.lstrip())
print("\nAfter using rstrip():") #RSTRIP
print(name.rstrip())
print("\nAfter using strip():") #STRIP
print(name.strip())
